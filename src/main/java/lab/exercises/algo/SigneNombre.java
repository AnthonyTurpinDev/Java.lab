package lab.exercises.algo;

public class SigneNombre {
    public static void main(String[] args) {
        int nombre = 0;

        if (nombre > 0) {
            System.out.println("Positif");
        } else if (nombre < 0) {
            System.out.println("Négatif");
        } else {
            System.out.println("Zéro");
        }
    }
}
