public class Exo4Chaines {
    public static void main(String[] args) {
        String texte = "java c'est super";
        
        // À TOI DE JOUER :
        // 1. Affiche le texte en MAJUSCULEs
        System.out.println(texte.toUpperCase());
        
        // 2. Affiche la longueur du texte
        System.out.println(texte.length());
        
        // 3. Remplace "super" par "génial" et affiche le résultat
        System.out.println(texte.replace("super", "génial"));
        
    }
}

